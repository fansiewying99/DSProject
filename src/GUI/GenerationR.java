        if(num>Main.it.getGenerationSize()||num<=0){
            Main.it.play("sound//error.wav");
            jTextField2.setText("invalid");
            jTextField3.setText("invalid");
        }
        else{
            Main.it.play("sound//ka-ching.wav");
            jTextField2.setText(Integer.toString(num));
            jTextField3.setText(Double.toString(Main.it.getGenerationRenevue(num)));
        }
